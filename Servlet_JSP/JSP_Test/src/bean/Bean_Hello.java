package bean;

public class Bean_Hello {
	public Bean_Hello() {}
	
	private String name = null;
	private String gender = null;

	public String getName() {
		return this.name;
	}
	
	public void setName(String n) {
		this.name = n;
	}
	
	public String getGender() {
		return this.gender;
	}
	
	public void setGender(String g) {
		this.gender = g;
	}
	
}