
import java.io.*;
import javax.servlet.*;
import javax.servlet.http.*;
import java.util.*;

//获取所有的参数

public class ShowParameters extends HttpServlet {
	public void doGet(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		response.setContentType("text/html");
		PrintWriter pw = response.getWriter();
		pw.println("<html> \n <head> \n" 
				+ "  <meta http-equiv=\"pragna\" content=\"no-cache\"></meta> \n"
				+ "  <title>Show Parameters</title> \n"
				+ " </head> \n"
				+ "<body> \n <table align=\"center\" border=\"1\"> \n"
				+ "  <tr bgcolor=#ff9966> \n " 
				+ "   <th>Parameter name  <th>Parameter value \n" 
				+ "  </tr>" );
		int c = 1;
		Enumeration paraNames = request.getParameterNames();
		while (paraNames.hasMoreElements()) {
			String pn = (String)paraNames.nextElement();
			String[] paraValues = request.getParameterValues(pn);//如果有多个值，那就检测paraValues.length.挨个儿显示。
			pw.println("<tr> \n <td> \n");
			pw.println(pn + " \n </td> \n");
			pw.println("<td> " + paraValues[0] + " \n");
			pw.println(" </td> \n <td> " + c + " </tr> \n");
			++c;
		}
		
		pw.println("</table></body></html>");
		
	}

	public void doPost(HttpServletRequest request, HttpServletResponse response)
			throws ServletException, IOException {
		doGet(request, response);
	}
}




















































































































































































































































































































































































































































































































































































































